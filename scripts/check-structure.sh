#!/usr/bin/env bash
# check-structure.sh — 结构反模式扫描（sharp-writing 审读分支 L0.5）
# 用法: check-structure.sh <文件.md>
# 词表 lint 管词，本脚本管句式结构。命中处需人工复核：
# 引用原话、规则讲解、反例展示可豁免；正文命中必改。
# 退出码: 0 = 无命中; 1 = 有命中（需人工复核）; 2 = 用法错误

set -u

if [ $# -lt 1 ] || [ ! -f "${1:-}" ]; then
  echo "用法: $0 <文件.md>" >&2
  exit 2
fi

f="$1"
hits=0

scan() {
  local label="$1" pattern="$2" advice="$3"
  local out
  out=$(grep -nE "$pattern" "$f" || true)
  if [ -n "$out" ]; then
    echo "【${label}】${advice}"
    echo "$out" | sed 's/^/  /'
    hits=$((hits + $(echo "$out" | wc -l | tr -d ' ')))
    echo
  fi
}

# 1. 二元对比骨架（判断句写正题不写反题；显式否定全篇 ≤1 处且仅用于纠偏活跃误解）
scan "二元对比骨架" \
  "不是[^，。；：]{1,15}，?(而是|是)[^，。]|不在[^，。；：]{1,10}，?(而)?在[^，。]|不再是[^，。；：]{1,15}|与其[^，。；]{1,15}，?不如|[^，。；：]{1,12}而非[^，。；]{1,12}|并非[^，。；]{1,12}" \
  "改法：正面陈述新判断 + 冒号 + 数字证据，对比交给数字并置"

# 2. 总结式收尾（空总结句直接删）
# 中文 markdown 一段就是一行，收尾词多在行中句首——锚定行首或句读之后，不能只锚行首
scan "总结式收尾" \
  "综上所述|(^|[。！？；：])[[:space:]]*(综上|总之|总而言之|归根结底|概括来看|整体来说|整体而言|本质上说|本质上讲)[，：]" \
  "改法：删掉总结引导词，直接说结论；结论没新信息就整句删"

# 3. 无源断言（借权威背书的判断必须有出处，否则改为自己扛）
scan "无源断言" \
  "已成(业界|行业)?(共识|标配|定局)|已经成为(业界|行业)(共识|标配)|业界(普遍|一致)认为|众所周知|毋庸置疑|不言而喻" \
  "改法：补出处，或改为自己的判断（\"是我们的基本判断\"）"

# 4. 对仗金句密度提示（全篇 ≤1 处；此项只计数不定位对错）
# 按出现次数计数（grep -o），不是按行数（-c）——同一段里的两处排比必须都算上；行尾无标点也算
DUISHU_RE="从[^，。；]{1,12}到[^，。；]{1,12}([，。；]|$)"
duishu=$(grep -oE "$DUISHU_RE" "$f" | wc -l | tr -d ' ')
if [ "${duishu:-0}" -gt 1 ]; then
  echo "【对仗密度】\"从 X 到 Y\"句式共 ${duishu} 处（限额 1 处），请复核保留信息密度最高的一处"
  grep -nE "$DUISHU_RE" "$f" | sed 's/^/  /'
  hits=$((hits + 1))
  echo
fi

if [ "$hits" -eq 0 ]; then
  echo "PASS: 结构反模式零命中（二元骨架/总结收尾/无源断言/对仗密度）"
  exit 0
else
  echo "共 ${hits} 处命中。人工复核：引用/规则讲解/反例可豁免，正文命中必改。"
  exit 1
fi
